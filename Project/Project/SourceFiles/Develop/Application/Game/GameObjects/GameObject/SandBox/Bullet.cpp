/*-----------------------------------------------------------------------------
/* コンストラクタ
-----------------------------------------------------------------------------*/
Bullet::Bullet(Game* game)
	: SandBox(game)
	, sphere_collider_(nullptr)
	, sphere_collider_gizmo_(nullptr)
	, obb_collider_(nullptr)
	, obb_collider_gizmo_(nullptr)
{
	this->Init();

	// バレットのマネージャのインスタンスのアドレスを追加
	game_->GetBulletManager()->AddBulletGameObjectAddress(this);
}

/*-----------------------------------------------------------------------------
/* デストラクタ
-----------------------------------------------------------------------------*/
Bullet::~Bullet(void)
{
	// バレットのマネージャのインスタンスのアドレスを削除
	game_->GetBulletManager()->RemoveBulletGameObjectAddress(this);

	this->Uninit();
}

