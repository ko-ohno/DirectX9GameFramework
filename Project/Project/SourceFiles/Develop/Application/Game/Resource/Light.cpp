#include "../../../StdAfx.h"
#include "Light.h"
#include "../Manager/LightManager.h"

/*-----------------------------------------------------------------------------
/* コンストラクタ
-----------------------------------------------------------------------------*/
Light::Light(LightManager* manager)
	: light_manager(manager)
	, light_type_(LightType::None)
{
	//自身の初期化
	this->Init();
}

/*-----------------------------------------------------------------------------
/* デストラクタ
-----------------------------------------------------------------------------*/
Light::~Light(void)
{
}

/*-----------------------------------------------------------------------------
/* 初期化処理
-----------------------------------------------------------------------------*/
bool Light::Init(void)
{
	// 初期化
	ZeroMemory(&light_, sizeof(light_));
	ZeroMemory(&d3d_material_, sizeof(d3d_material_));

	//ライト自身の力強さ
	light_strength_	= 1.0f;	

	//ライトの色を初期化
	{
		//拡散反射光
		diffuse_  = D3DXVECTOR4(0.3f, 0.3f, 0.3f, 1.0f);

		//環境光
		ambient_  = D3DXVECTOR4(1.f, 1.f, 1.f, 1.f );

		//鏡面反射光
		specular_ = D3DXVECTOR4(1.f, 1.f, 1.f, 1.f);

		//自己発光色
		emissive_ = D3DXVECTOR4(0.5f, 0.5f, 0.5f, 0.5f);
	}
	return true;
}

/*-----------------------------------------------------------------------------
/* 終了化処理
-----------------------------------------------------------------------------*/
void Light::Uninit(void)
{
}

